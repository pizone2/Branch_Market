package com.main.branch.inquiry;

public class InquiryDTO {
	private Integer r;
	
	private Integer inquiryNum;
	private String memberId;
	private String inquiryTitle;
	private String inquiryDetail;
	private String inquiryCategory;
	
	
	
	public Integer getR() {
		return r;
	}
	public void setR(Integer r) {
		this.r = r;
	}
	public Integer getInquiryNum() {
		return inquiryNum;
	}
	public void setInquiryNum(Integer inquiryNum) {
		this.inquiryNum = inquiryNum;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getInquiryTitle() {
		return inquiryTitle;
	}
	public void setInquiryTitle(String inquiryTitle) {
		this.inquiryTitle = inquiryTitle;
	}
	public String getInquiryDetail() {
		return inquiryDetail;
	}
	public void setInquiryDetail(String inquiryDetail) {
		this.inquiryDetail = inquiryDetail;
	}
	public String getInquiryCategory() {
		return inquiryCategory;
	}
	public void setInquiryCategory(String inquiryCategory) {
		this.inquiryCategory = inquiryCategory;
	}
	
}
